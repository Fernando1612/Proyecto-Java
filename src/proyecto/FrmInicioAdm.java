    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        Libro libro = new Libro();
        String ISBN = JOptionPane.showInputDialog("Ingrese el ISBN del libro:");
        libro.setISB(ISBN);
        String titulo = JOptionPane.showInputDialog("Ingrese el nombre del libro:");
        libro.setTitulo(titulo);
        String autor = JOptionPane.showInputDialog("Ingrese el nombre del autor:");
        libro.setAutor(autor);
        String precio = JOptionPane.showInputDialog("Ingrese el precio del libro:");
        libro.setPrecio(precio);
        
        FileWriter flwriter = null;
        try{
            flwriter = new FileWriter("D:/libros.txt", true);
            BufferedWriter bfwrite = new BufferedWriter(flwriter);
            bfwrite.write( libro.getTitulo() + "\n" );
            bfwrite.close();
            JOptionPane.showMessageDialog(null, "Libro agregado correctamente.");
        }catch(IOException e){
            e.printStackTrace();
        }finally{
            if(flwriter != null){
                try{
                    flwriter.close();
                }catch(IOException e){
                    e.printStackTrace();
                }
            }
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnArticulosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnArticulosActionPerformed
        // TODO add your handling code here:
        FileReader fr = null;
        try {
            // TODO add your handling code here:
            int nLineas = 0;
            int i = 0;
            String [] libros = null;
            String linea;
            sc = new Scanner(new File ("D:/libros.txt"));
            File f = new File("D:/libros.txt");
            fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            
            try {
                while((linea =  br.readLine()) != null){
                    nLineas++;
                }
            } catch (IOException ex) {
                ex.printStackTrace();
            }
            
            libros = new String[nLineas];//Tama√±o del arreglo
            
            while(sc.hasNextLine()){
                libros[i++] = sc.nextLine(); //almacenamos cada linea en una posicion del arreglo
            }
        Herramientas m = new Herramientas();
        m.mostrarLibros(libros);
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "No se encontro el archivo");
            ex.printStackTrace();
        } finally {
            try {
                sc.close();
                fr.close();
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnArticulosActionPerformed

    private void btnVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVenderActionPerformed
        // TODO add your handling code here:
        String nombre = JOptionPane.showInputDialog("Nombre del libro");
        File arcViejo = new File("D:/libros.txt");
        File nvoArc = new File("D:/tmp.txt");
        try{
            BufferedReader leer = new BufferedReader(new FileReader(arcViejo));
            BufferedWriter escribir = new BufferedWriter(new FileWriter(nvoArc));
            
            String linea;
            
            while((linea = leer.readLine()) != null){
                String trmLinea = linea.trim();
                if(trmLinea.equals(nombre)){
                    continue;
                }else{
                }
                escribir.write(linea + System.getProperty("line.separator"));
            }
            escribir.close();
            leer.close();
            arcViejo.delete();
            File basura = new File("D:/libros.txt");
            nvoArc.renameTo(basura);
        } catch (IOException ex) {
            ex.printStackTrace();
        } 
    }//GEN-LAST:event_btnVenderActionPerformed

